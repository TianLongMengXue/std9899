#include <stdio.h>
int main () {
	char a = 60;
	char b = -13;
	char c = 30;
    /*
		a   = 0011 1100 (二进制补码)
		b   = 1111 0011 (二进制补码)
		a|b = 1111 1111 (二进制补码)
			  1000 0001 = -1
	*/
	printf("a|b = %d\n", a|b); // -1
	/*
		a	= 0011 1100 (二进制补码)
		c	= 0001 1110 (二进制补码)
		a|c = 0011 1110 (二进制补码) = 62
	*/
	printf("a|c = %d\n", a|c); // 62
    return 0;
}
